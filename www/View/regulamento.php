<h2>Política de entrega</h2>

<p>As entregas feitas pelo PET CUSTOMS são realizadas de segunda-feira a sexta-feira, das 8h às 21h e sábado das 9h ás 16h</p>

<p>Após a confirmação do pagamento, o prazo para emissão da Nota Fiscal é de até 2 dias úteis (Não inclui sábados, domingos e feriados).</p>

<p>O serviço de entrega rápida abrange apenas o estado do Rio de Janeiro, para compras até ás 12h, sendo assim, a entrega será feita no mesmo dia
    incluindo a emissão da Nota Fiscal.
</p>

<p>Fatores naturais e adversos: chuvas fortes, acidentes ou greves na região onde o produto deveria ser entregue impossibilitam
    a chegada do transportador.</p>

<p>Em caso de ausência do destinatário, as transportadoras têm o compromisso de realizar até três tentativas de entrega no local informado,
    em dias alternados, com intervalos de até 48 horas entre uma e outra. É indispensável que, no endereço solicitado, tenha uma pessoa 
    autorizada pelo comprador(a), maior de 18 anos, e portando documento de identificação para receber a mercadoria e assinar o protocolo.</p>
    
<p>Após a finalização do pedido <b>NÃO</b> é possível alterar a forma de entrega, solicitar adiantamento informando no campo "CONTATOS". 
    A cada atualização no status do pedido nosso sistema envia, automaticamente, e-mails de alerta. Por isso, é importante manter 
    seu e-mail atualizado no cadastro.</p>

<h2>Quando recusar o produto</h2>

<p>Se ocorrer qualquer das hipóteses abaixo, recuse o recebimento da mercadoria e escreva o motivo da recusa no verso do DANFE:</p>

    <p>• embalagem aberta ou avariada;</p>
    
    <p>• produto avariado;</p>
    
    <p>• produto em desacordo com o pedido;</p>
    
    <p>• falta de acessórios.</p>
    
<p>Se, ainda assim, você aceitar o produto, por favor, entre em contato com o setor de atendimento em até 48 horas.
    Para solicitar a devolução, envie uma mensagem no setor CONTATOS.</p>

<h2>Acompanhe seu pedido</h2>

<p>Após fazer login no Pet Customs você será direcionado(a) ao "Minha Conta", no qual pode acompanhar a aprovação do pagamento, 
    a entrega do produto à transportadora e seu trajeto até o endereço informado.
    Nosso sistema envia, automaticamente, e-mails de alerta notificando qualquer alteração no processamento do pedido. Ao se cadastrar, 
    informe um e-mail válido para que possa receber esses alertas.</p>

<h2>Não recebimento da compra</h2>

<p>Verifique abaixo, o que pode ter acontecido:</p>

    <p>• Data de entrega: verifique se a data prevista de entrega já expirou.</p>
    
    <p>Se a data de entrega do pedido passou, entre em contato com o nosso setor de atendimento.</p>
    
    <p>• Dados do endereço: confirme se o endereço de entrega está correto em seu cadastro. Para sua segurança, nossos operadores
     não podem alterar dados cadastrais dos clientes.</p>
    
    <p>Se houver qualquer divergência pedimos que atualize o endereço de entrega em seu cadastro e entre em contato com o nosso setor
     de contatos.</p>
    
    <p>• Pagamento: confirme se o pagamento já foi aprovado. O prazo de aprovação do pagamento varia de acordo com a forma em que foi efetuado.
     Para verificar o prazo de aprovação de cada forma de pagamento clique aqui.</p>
    
    <p>• Ausência de recebedor: verifique o status do pedido. Caso conste a informação de que o Pet Customs já fez alguma tentativa sem
        sucesso de entrega, pedimos que aguarde de 24h a 48h. Temos o compromisso de fazer até três tentativas de entrega consecutivas.</p>
    
    <p>Se houver três tentativas de entrega sem sucesso, encaminharemos o(s) produto(s) ao Centro de Distribuição e enviaremos uma notificação.</p>
    
    <p>• Caso more em apartamento, verifique se o produto foi entregue na portaria.</p>

    <p>• Recusa do produto: certifique-se de que as pessoas encarregadas de receber o produto, como porteiros ou empregados, não se recusaram
        a recebê-lo. Nesse caso, entre contato com o nosso setor de contatos.</p>

<h2>Formas de pagamento</h2>

<p><b>Cartão de crédito</b></p>

    <p>Em caso de pagamento em cartão de crédito, o prazo para entrega é considerado a partir da confirmação do pagamento pela 
        administradora do cartão, o que pode ocorrer em até 2 (dois) corridos. Caso sendo do estado do Rio de Janeiro abrangendo o serviço
    de entrega rápida (Política de entrega-linha 3) comunicaremos via e-mail ou telefone o motivo.</p>
        
    <p>Para ampliar ainda mais sua segurança, os pedidos pagos com cartão de crédito passam por uma checagem de dados cadastrais.
        Se houver divergência cadastral, o Pet Customs entrará em contato com você.</p>
        
    <p><b>Boleto bancário</b></p>
        
    <p>Em caso de pagamento com boleto bancário, o Pet Customs libera o pedido mediante a confirmação do pagamento pelo banco. 
        A confirmação é realizada em até três dias úteis, a partir do pagamento do boleto.</p>

